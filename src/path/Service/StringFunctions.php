    private function sanitize_title_with_dashes( $title ) {
        $title = strip_tags($title);
        // Preserve escaped octets.
        $title = preg_replace('|%([a-fA-F0-9][a-fA-F0-9])|', '---$1---', $title);
        // Remove percent signs that are not part of an octet.
        $title = str_replace('%', '', $title);
        // Restore octets.
        $title = preg_replace('|---([a-fA-F0-9][a-fA-F0-9])---|', '%$1', $title);

        if ($this->seems_utf8($title)) {
            if (function_exists('mb_strtolower')) {
                $title = mb_strtolower($title, 'UTF-8');
            }
            $title = $this->utf8_uri_encode($title, 200);
        }

        $title = strtolower($title);

        //if ( 'save' == $context ) {
        // Convert nbsp, ndash and mdash to hyphens
        $title = str_replace( array( '%c2%a0', '%e2%80%93', '%e2%80%94','%e2%80%8c' ), '-', $title );
        // Convert nbsp, ndash and mdash HTML entities to hyphens
        $title = str_replace( array( '&nbsp;', '&#160;', '&ndash;', '&#8211;', '&mdash;', '&#8212;' ), '-', $title );

        // Strip these characters entirely
        $title = str_replace( array(
            // iexcl and iquest
            '%c2%a1', '%c2%bf',
            // angle quotes
            '%c2%ab', '%c2%bb', '%e2%80%b9', '%e2%80%ba',
            // curly quotes
            '%e2%80%98', '%e2%80%99', '%e2%80%9c', '%e2%80%9d',
            '%e2%80%9a', '%e2%80%9b', '%e2%80%9e', '%e2%80%9f',
            // copy, reg, deg, hellip and trade
            '%c2%a9', '%c2%ae', '%c2%b0', '%e2%80%a6', '%e2%84%a2',
            // acute accents
            '%c2%b4', '%cb%8a', '%cc%81', '%cd%81',
            // grave accent, macron, caron
            '%cc%80', '%cc%84', '%cc%8c',
        ), '', $title );


        // Convert times to x
        $title = str_replace( '%c3%97', 'x', $title );
        //}

        $title = preg_replace('/&.+?;/', '', $title); // kill entities
        $title = str_replace('.', '-', $title);

        $title = preg_replace('/[^%a-z0-9 _-]/', '', $title);
        $title = preg_replace('/\s+/', '-', $title);
        $title = preg_replace('|-+|', '-', $title);
        $title = trim($title, '-');

        return $title;
    }

    public function convertStringToSlug( $title ) {

        $title = $this->remove_accents($title);

        $title = $this->sanitize_title_with_dashes($title);

        /*if ( '' === $title || false === $title )
            $title = $fallback_title;*/

        return urldecode($title);
    }

    public function getRealIPAddr(Request $request){

        $ip = null;

        if (!empty($request->server->get('HTTP_CLIENT_IP'))) { //check ip from share internet

            $ip=$request->server->get('HTTP_CLIENT_IP');

        } elseif (!empty($request->server->get('HTTP_X_FORWARDED_FOR'))) { //to check ip is pass from proxy

            $ip=$request->server->get('HTTP_X_FORWARDED_FOR');

        } elseif (!empty($request->server->get('REMOTE_ADDR'))) {

            $ip=$request->server->get('REMOTE_ADDR');

        } else {

            $ip=$request->server->get('AR_REAL_IP');

        }

        return $ip;
    }

