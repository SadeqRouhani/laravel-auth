    public static function validateNationalId($nationalId){
        //return true;
        if (!$nationalId){
            return false;
        }
        $nationalId = self::convertNumbersToEnglish($nationalId);
        if (!preg_match("/^\d{10}$/",$nationalId)){
            return false;
        }
        $sum = 0;
        for ($i=0;$i<=8;$i++) {
            $sum += (10-$i)*substr($nationalId,$i,1);
        }

        $n = $sum%11;
        if ($n>=2){
            $n = 11-$n;
        }

        if ((int) substr($nationalId,9,1) === $n){
            return true;
        }else{
            return false;
        }
    }

