    public static function convertPersianStringDateToDatetime ($persianStringDate,$timezone = 'Asia/Tehran')
    {
        if (!$persianStringDate) {
            return null;
        }

        $persianStringDate = self::convertNumbersToEnglish($persianStringDate);
        $jDateTime = new jDateTime(false,true,$timezone);

        $dateTimeArray = explode('-', $persianStringDate);
        $timeArray = $dateTimeArray[1] ?? [];
        $dateArray = $dateTimeArray[0] ?? [];

        $dateArray = explode('/', $dateArray);
        $year = isset($dateArray[0]) ? (int)$dateArray[0] : 0;
        $month = isset($dateArray[1]) ? (int)$dateArray[1] : 0;
        $day = isset($dateArray[2]) ? (int)$dateArray[2] : 0;

        if (!$year || !$month || !$day){
            return  null;
        }

        $hour = $minute = $second = 0;

        if ($timeArray) {
            $timeArray = explode(':', $timeArray);
            $hour = isset($timeArray[0]) ?  (int)$timeArray[0] : 0;
            $minute = isset($timeArray[1]) ?  (int)$timeArray[1] : 0;
            $second = isset($timeArray[2]) ?  (int)$timeArray[2] : 0;
        }


        $tz = date_default_timezone_get();
        date_default_timezone_set('Asia/Tehran');
        $date = $jDateTime->mktime($hour,$minute,$second,$month,$day,$year);
        $date = new \DateTime('@'.$date);
        date_default_timezone_set($tz);

        return  $date;
    }

    public  function minToTime($min) {
        $hours = floor($min/60);
        $minutes = fmod($min,60);

        return ($hours < 10 ? '0'.$hours : $hours).':'.($minutes < 10 ? '0'.$minutes : $minutes);
    }

    public static function secondToTime($inputSeconds)
    {
        $hour = (int)($inputSeconds / 3600);
        $minute = (int)(($inputSeconds - $hour * 3600) / 60);
        $second = (int)($inputSeconds - $hour * 3600 - $minute * 60);

        return self::convertNumbersToFarsi((($hour < 10 and $hour > 0) ? '0' : '') . ($hour > 0 ? ($hour . ':') : '') . (($minute < 10) ? '0' : '') . $minute . ':' . (($second < 10) ? '0' : '') . $second);
    }

